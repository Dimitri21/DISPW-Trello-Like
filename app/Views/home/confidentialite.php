    <h2>Politique en matière de cookies </h2>

    <p>Les sites de la Société et toutes ses pages Web utilisent des cookies pour vous distinguer des autres  utilisateurs  du  site.  Cela  nous  permet  d'améliorer  les  services  et  d'optimiser  votre expérience  utilisateur.Un  cookie  consiste  en  une  chaîne  d'informations  au  format  texte transférée  par  un  site  Web  vers  le  disque  dur  de  votre  ordinateur  afin  que  le  site  Web  se souvienne  de  vous.  Les  cookies  peuvent  aider  un  site  Web  à  adapter  plus  rapidement  le contenu à vos centres d'intérêt. La plupart des sites Web importants utilisent des cookies. En général,  un  cookie  contient  le  nom  du  domaine  d'origine  du  cookie  ;  la  "durée  de  vie"  du cookie et une valeur, à savoir un numéro unique créé de façon aléatoire.</p>

    <p>Sur les sites de la Société, il existe deux types de cookies :</p>

    <ul>
        <li>des  cookies  temporaires,  qui  demeurent  dans  le  fichier  cookie  de  votre  navigateur jusqu'à ce que vous quittiez le site</li>
        <li>des cookies persistants, qui demeurent beaucoup plus longtemps dans le fichier cookie de votre navigateur (la durée dépend de la durée de vie du cookie en question).</li>
    </ul>

    <p>Les cookies temporaires sont utilisés :</p>

    <ul>
        <li>pour vous permettre de transférer des informations d'une page à l'autre de notre site sansavoir à les saisir à nouveau</li>
        <li>pour vous permettre d'accéder aux informations d'enregistrement conservées.</li>
    </ul>

    <p>Les cookies persistants sont utilisés : </p>

    <ul>
        <li>pour  nous  aider  à  vous  identifier  en  tant  que  visiteur  (à  l'aide  d'un  numéro,  pas personnellement) lorsque vous revenez sur notre site Web</li>
        <li>Pour compiler des statistiques globales anonymes qui nous permettent de comprendre comment les visiteurs utilisent notre site et nous aident à améliorer la structure de notre site Web. Nous ne pouvons pas vous identifier personnellement de cette façon. </li>
    </ul>

    <p>Vous avez la possibilité d'accepter ou de refuser les cookies en modifiant les paramètres de votre navigateur. Cependant, vous risquez de perdre certaines fonctions interactives du service si les cookies sont désactivés.</p>

    <p>Vous pouvez supprimer et bloquer tous les cookies en provenance des sites de la Société. Pour ce faire, modifiez le paramètre de votre navigateur permettant de refuser certains ou tous les cookies.  Sachez  cependant  que  si  vous  décidez  de  bloquer  les  cookies  via  votre  navigateur, certains éléments de notre site Web risquent de ne pas fonctionner correctement. </p>

</article>


